package prototypecelular22;

/**
 *
 * @author Carlos
 */
public class TamanoPantalla {
    int alto;
    int ancho;

    public TamanoPantalla(int alto, int ancho) {
        this.alto = alto;
        this.ancho = ancho;
    }

    public int getAlto() {
        return alto;
    }

    public void setAlto(int alto) {
        this.alto = alto;
    }

    public int getAncho() {
        return ancho;
    }

    public void setAncho(int ancho) {
        this.ancho = ancho;
    }
    
}
